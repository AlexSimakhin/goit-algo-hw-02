def check_delimiters(input_string):
    # Стек для зберігання відкриваючих дужок
    stack = []
    # Словник для перевірки відповідності закриваючих і відкриваючих дужок
    matching_brackets = {")": "(", "]": "[", "}": "{"}

    # Проходимо по кожному символу рядка
    for char in input_string:
        # Якщо це відкриваюча дужка, додаємо її у стек
        if char in "({[":
            stack.append(char)
        # Якщо це закриваюча дужка
        elif char in ")}]":
            # Якщо стек порожній або немає відповідності між дужками, повертаємо "Несиметрично"
            if not stack or matching_brackets[char] != stack.pop():
                return "Несиметрично"

    # Якщо стек порожній після обробки рядка, дужки симетричні
    return "Симетрично" if not stack else "Несиметрично"

# Приклади тестування
test_cases = ["( ){[ 1 ]( 1 + 3 )( ){ }}", "( 23 ( 2 - 3);", "( 11 }", "( ) { [ ] ( ) ( ) { } } }", "(2)[]{}", "([$)]", "{[!!!]}", "((())", "([)]"]

# Перевіряємо кожен тестовий випадок
for test in test_cases:
    result = check_delimiters(test)
    print(f"{test}: {result}")
